float32_t myAtan(float32_t x)
{
	int i;
	float32_t index;
	int index_int;
	float32_t index_dec;


	if (x<0.0f) return -myAtan(-x);
	if (x == 0.0f)   return 0.0f;
	for (i = 1; i<=BLOCK_NUM_ATAN; i++)
	{
		if (x <= atan_table_delimit_val[i])
		{
			index = DEBIDE_ONE_BLOCK_ATAN * (i - 1) + ((x - atan_table_delimit_val[i - 1]) / atan_table_width[i - 1]);
			index_int = (int)index;
			index_dec = index - (float32_t)index_int;
			return atan_table[index_int] + index_dec * ((atan_table[index_int + 1] - atan_table[index_int]));
		}
	}
	return atan_table[TABLE_SIXE_ATAN-1];   

}

float32_t myAtan2(float32_t y, float32_t x)
{
	if (x >  0.0f) return myAtan(y / x);
	if (y >= 0.0f &&  x <  0.0f) return myAtan(y / x) + PI;
	if (y <  0.0f &&  x <  0.0f) return myAtan(y / x) - PI;
	if (y >  0.0f &&  x == 0.0f) return PI / 2.0f;
	if (y <  0.0f &&  x == 0.0f) return -PI / 2.0f;
	if (y == 0.0f &&  x == 0.0f) return 0.0f;  //普通は定義されない.
	return 0.0f;
}

float32_t mypowf(float32_t src,uint32_t n)
{
  int i;
  float32_t csrc;
  csrc = src;
  for(i=0;i<n-1;i++){
    src *= csrc;
  }
  return src;
}

void round_3(float32_t* val)
{
  int a;
  if(*val > 0){
    a = (int)((*val)*10.0f + 0.5f);
    *val = (float32_t)a / 10.0f;
    if(*val < 0.1f)*val = 0.0f;
  }else{
    a = (int)((*val)*10.0f-0.4f);
    *val = (float32_t)a / 10.0f;
    if(*val > -0.1f)*val = 0.0f;
  }
}
